function fecharModal()
{
  document.getElementById('fundo').style.display = 'none';
  document.getElementById('modal1','modal2','modal3').style.display = 'none';
}

function abrirModal()
{
  document.getElementById('fundo').style.display = 'block';
  document.getElementById('modal1','modal2','modal3').style.display = 'block';
}